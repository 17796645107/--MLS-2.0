package com.dn.domain;
//商品菜单实体类
public class Menu {

	private Integer id;
	private String name;
	
	public Menu() {}
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
